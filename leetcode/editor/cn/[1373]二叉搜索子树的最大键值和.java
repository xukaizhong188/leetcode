/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    int result = 0;
    Map<TreeNode,Integer> nodeMap = new HashMap();
    public int maxSumBST(TreeNode root) {
        getMax(root);
        return result;
    }
    public void getMax(TreeNode root){
        if (root == null){
            return ;
        }
        Integer rootSum = null;
        if (nodeMap.get(root) != null){
            rootSum = nodeMap.get(root);
        }else {
            rootSum = isBST(root,null,null);
        }
        //åˆ¤æ–­è‡ªå·±æ˜¯å¦ä¸ºBST
        //Integer rootSum = isBST(root,null,null);
        if (rootSum != null ){
            if (rootSum >= result){
                result = rootSum;
            }
        }
        getMax(root.left);
        getMax(root.right);
    }
    /**
     * æ ¡éªŒä¸€ä¸ªæ ‘æ˜¯å¦ä¸ºäºŒå‰æœç´¢æ ‘
     * @param root
     * @param min
     * @param max
     * @return
     */

    public Integer isBST(TreeNode root,TreeNode min,TreeNode max){
        int sum = 0;
        if (root == null){
            return 0;
        }
        if (min != null && root.val <= min.val){
            return null;
        }
        if (max != null && root.val >= max.val){
            return null;
        }
        Integer left = isBST(root.left,min,root);
        Integer right = isBST(root.right,root,max);
        if (left == null || right == null){
            return null;
        }
        sum =  root.val + left +right;
        nodeMap.put(root,sum);
        return sum;
    }

}
